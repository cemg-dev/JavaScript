/*
* . Em JavaScript declaramos variáveis como var ou let
*     Detalhes:
*         . É recomendado usar let, pois foi implementado como solução de alguns problemas em var
*/
    let n1 = 0;         // int
    let n2 = 123456789; // long
    let n3 = 1.2345;    // float
    let n4 = 1.2345678; // double
    let c1 = 'a';       // char
    
/*
* . Cada variável: n1, n2, n3, n4 e n5 foi declarada usando let, mas armazenam dados de diferentes tipos;
* . Cada variável só poderá armazenar valores correspondentes ao seu tipo;
* . Cada variável foi declarada e inicializada na mesma linha;
*/
    
/*
* . Declarando variáveis sem inicializá-las;
* . Ao fazer isto as variavéis assumem uma espécie de tipo chamado undefined
*/
let n6;
let n7;
let n8;
let n9;
let c2;
    
/*
* . É possível declarar multiplas variáveis do mesmo tipo em apenas uma linha;
*/
let n10 = 10.21222212, n11, n12;

